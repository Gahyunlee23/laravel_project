2. 미동의 시 불이익 사항
마케팅 정보 수신 동의를 거부하실 경우 신규 호텔 오픈 소식, 프로모션 특가 소식, 고객 대상 이벤트 소식 등 혜택 사항 적용이 불가할 수 있습니다.
개인정보보호법 제22조 제5항에 의해 선택 정보 사항에 대해서는 동의 거부하시더라도 서비스 이용에 제한되지 않습니다.

