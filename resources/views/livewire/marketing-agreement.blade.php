<div class="w-full px-4 pb-4 h-32 whitespace-pre-line">1. 광고성 정보의 이용 목적
신규 서비스(제품) 개발 및 맞춤 서비스 제공, 이벤트 및 광고성 정보 제공 및 참여 기회 제공, 신규 소식 안내 및 광고 게재 등을 목적으로 개인 정보를 처리합니다.
