    가. 필수기재 정보: ID(전자우편주소) 및 비밀번호, 이름, 휴대전화번호
    나. 선택기재 정보: 생년월일, 성별
    2. “호텔”은 “회사”가 정한 가입 양식에 따라 다음 각 호의 호텔정보를 기입한 후 이 약관에 동의한다는 의사표시를 함으로써 회원가입을 신청합니다.
    가. 필수기재 정보: ID(전자우편주소) 및 비밀번호, 이름, 휴대전화번호
