    제1조(목적)
    이 약관은 주식회사 트래블메이커스(이하 “회사”)가 운영하는 웹사이트 또는 어플리케이션(이하 “호텔에삶 플랫폼”)을 통하여 구독자(이하 “구독자”) 및 일반 이용고객(이하 “이용자”)에게 제공되는 구독 서비스 및 이용권(이하 “서비스”) 관련 권리 및 의무 일반에 대한 법률관계를 명확히 함을 목적으로 합니다.

    제2조(용어의 정의)
    이 약관에서 사용되는 용어의 정의는 다음 각 호와 같습니다.
    1. “호텔”은 회사의 이용자에게 제공하는 회사와 계약된 서비스 대상 시설물을 말합니다.
    2. “이용자”는 회사의 호텔관련 구독 서비스 및 한달살기 서비스를 결제하여 이용하는 회원 및 비회원을 말합니다.
    3. “회원”은 회사가 정한 가입절차에 따라 회원등록을 한 개인 또는 사업자로서, 회사의 정보를 지속적으로 제공받으며 회사가 제공한 서비스를 이용할 수 있는 자를 말합니다.
    4. “비회원”은 회원에 가입하지 않고(SNS 계정을 통해 로그인하고 본인인증을 거치지 않은 경우 포함) 회사가 제공하는 서비스를 이용하는 자를 말합니다.
    3. “호텔에서 한달살기 서비스”는 회사가 계약한 호텔에서 회사의 이용권을 구매하여 정해진 기간동안 호텔에서 거주하는 서비스를 의미합니다.
    4. “구독 서비스”는 이용자가 회사의 정해진 기간동안 이용권에 규칙에 맞게 회사가 계약한 호텔을 자유롭게 이용하는 서비스를 의미합니다.

    제3조(약관의 명시와 개정)
    1. “회사”는 이 약관의 내용과 상호 및 대표자 성명, 영업소 소재지, 주소(소비자의 불만을 처리할 수 있는 곳의 주소를 포함), 전화번호, 전자우편주소, 사업자등록번호, 통신판매업신고번호, 개인정보관리책임자 등을 이용자가 쉽게 알 수 있도록 “회사” 웹사이트의 초기서비스화면에 게시합니다. 다만, 약관의 내용은 이용자가 연결화면을 통하여 볼 수 있도록 할 수 있습니다.
    2. “회사”는 전자상거래 등에서의 소비자 보호에 관한 법률, 약관의 규제에 관한 법률, 전자거래기본법, 전자서명법, 정보통신망 이용촉진 등에 관한 법률, 소비자기본법 등 관련법을 위배하지 않는 범위에서 이 약관을 개정할 수 있습니다.
    3. “회사”가 약관을 개정할 경우에는 적용일자 및 개정내용, 개정사유 등을 명시하여 현행 약관과 함께 “회사” 웹사이트의 초기화면이나 초기화면과의 연결화면에 그 적용일자 7일 이전부터 적용일자 전일까지 공지 혹은 7일 이전까지 회원 대상 이메일 발송하여 공지합니다. 다만, 회원에게 불리하게 약관내용을 변경하는 경우에는 최소한 30일 이전의 유예기간을 두고 공지합니다. 이 경우 “회사”는 중요내용에 관하여 개정 전 내용과 개정 후 내용을 명확하게 비교하여 회원이 알기 쉽도록 표시합니다.
    4. “회사”가 약관을 개정할 경우에는 그 개정약관은 그 적용일자 이후에 체결되는 계약에만 적용되고 그 이전에 이미 체결된 계약에 대해서는 개정 전의 약관조항이 그대로 적용됩니다. 다만 이용자가 개정약관 조항의 적용을 받기를 원하는 뜻을 제3항에 의한 개정약관의 공지기간 내에 “회사”에 송신하여 동의를 받는 경우에는 개정일자부터 개정약관 조항이 적용됩니다.
    5. “회사”가 약관을 개정하는 경우 이용자는 개정약관에 동의하지 않을 권리가 있습니다. 이용자가 개정약관에 동의하지 않을 경우에는 서비스 이용을 중단하고 회원인 경우 “회사”에 탙퇴를 요청할 수 있습니다. 다만 이용자가 제3항의 방법 등으로 고지한 개정약관의 개정 유예기간 내에 “회사”의 개정약관에 동의하지 않겠다는 명시적인 의사표시를 하지 않는 경우 개정 약관에 동의한 것으로 간주합니다.
    6. 이 약관은 “회사”와 이용자간에 성립되는 서비스 이용 관련 기본약정입니다. “회사”는 필요한 경우 특정 서비스에 관하여 적용될 사항(이하 “개별약관”이라 함)을 정하여 미리 공지할 수 있으며, 이러한 개별약관에 동의하고 특정 서비스를 이용하는 경우에는 개별약관이 우선적으로 적용되고, 이 약관은 보충적인 효력을 갖습니다. 개별약관의 변경에 관해서는 제3항 및 제4항을 준용합니다.
    7. 이용자가 가입시 동의한 약관에 관하여 열람을 요구할 경우, “회사”는 이용자가 가입시 혹은 결제 시 기재한 전자우편 / 개별 연락처로 링크 형태로 전송합니다.
    8. “회사”는 제공하는 서비스의 구체적인 내용에 따라 개별서비스에 대한 약관 및 이용조건을 각 개별 서비스마다 별도로 정하여 회원의 동의를 얻을 수 있습니다. 이 경우 개별 서비스에 대한 이용약관 등이 본 약관에 우선합니다.
    9. 이 약관과 개별약관에도 정하지 아니한 사항과 “회사”약관의 해석에 관하여는 전자상거래 등에서의 소비자보호에 관한 법률, 약관의 규제 등에 관한 법률, 정부가 제정한 전자 상거래 등에서의 소비자보호 지침 등 관계법령, 정부기관의 해석 또는 상관례에 따릅니다.

    제4조(서비스의 제공 및 변경)
    1. “회사”는 다음과 같은 업무를 수행합니다.
    가. “호텔”의 모집 및 관리
    나. “호텔”과 “이용자”간의 이용권 발행 및 이에 부수하는 결제·정산 관리
    다. 기타 “회사”가 정하는 업무 등
    2. “회사”가 제공하는 서비스의 내용을 기술적 환경 변화 등의 사유로 변경할 경우에는 그 사유를 이용자에게 공지의 방식으로 안내하여야 합니다.
    3. “회사”는 필요하다고 판단하는 경우 서비스의 일부를 제휴업체 등 제3자에 위탁할 수 있습니다.

    제5조(서비스의 중단)
    1. “회사”는 컴퓨터 등 정보통신설비의 보수 점검, 교체 및 고장, 통신의 두절, 서비스 이용의 폭주 및 국가비상사태, 정전, 천재지변, 코로나, 호텔의 영업중단 등의 불가항력적인 사유가 있는 경우 서비스의 제공을 일시적으로 중단할 수 있습니다.
    2. “회사”는 사업종목의 전환 및 포기 또는 업체간 통합, 서비스 고도화 등의 이유로 서비스를 제공할 수 없게 되는 경우에는 공지의 방식으로 이용자에게 해당 내용을 사전에 통지합니다.

